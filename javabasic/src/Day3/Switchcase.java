package Day3;

public class Switchcase {
	public static void main(String[] args) {
	int value =5;
	switch (value) {
	case 3:
		System.out.println("case 3 executed");
	    break;
	case 4:
		System.out.println("case 4 executed");
		break;
	case 5:
		System.out.println("case 5 executed");
		break;
	case 8:
		System.out.println("case 8 executed");
		break;
	default:
		System.out.println("none of your case match");
	}
		
		
		
	}

}
